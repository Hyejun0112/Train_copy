"""
배포용 zip 패키징 스크립트
사용법: python package.py        (먼저 python build.py 로 EXE를 만든 뒤 실행)

dist/Train_copy.exe + 사용법 안내 + 매핑양식 엑셀을 묶어
dist/Train_copy_v{버전}.zip 을 만든다. 이 zip 하나만 배포하면 된다.
"""

import os
import sys
import zipfile

VERSION  = "1.0"
APP_NAME = "Train_copy"

EXE_PATH      = os.path.join("dist", f"{APP_NAME}.exe")
TEMPLATE_XLSX = "Train_copy_매핑양식.xlsx"   # 있으면 같이 포함(없으면 건너뜀)
ZIP_PATH      = os.path.join("dist", f"{APP_NAME}_v{VERSION}.zip")

USAGE_TEXT = f"""Train_copy v{VERSION} — 사용법
================================================================

P&ID 도면의 Bluebeam 마크업을, 같은 설비의 다른 Train 도면으로
자동 복사해 주는 프로그램입니다.

----------------------------------------------------------------
[설치]
----------------------------------------------------------------
1. 이 zip 파일의 압축을 푸세요(반드시 풀고 실행해야 합니다).
   - 압축 안에서 바로 더블클릭하지 마세요(느리거나 오류 발생).
2. Train_copy.exe 를 더블클릭해 실행합니다.
   - 처음 실행 시 "Windows의 PC 보호" 파란 경고가 뜨면
     [추가 정보] → [실행] 을 누르세요(서명 안 된 프로그램이라 뜨는 정상 경고).
   - 백신이 차단하면 사내 IT에 화이트리스트(예외) 등록을 요청하세요.


================================================================
사용 방법
================================================================

----------------------------------------------------------------
1. 폴더 설정  (📁 폴더 설정)
----------------------------------------------------------------
화면 왼쪽 위 '폴더 설정'에서 세 폴더를 각각 지정합니다.

  • Source 폴더 : 마크업이 들어있는 "원본" PDF가 있는 폴더
  • Target 폴더 : 마크업을 받을 "대상" PDF가 있는 폴더
  • Output 폴더 : 결과 PDF가 저장될 폴더(비어 있어도 됨)

각 줄의 [찾아보기] 버튼으로 폴더를 고르면, 아래 탭에 폴더 안
PDF 목록이 표시됩니다.


----------------------------------------------------------------
2. 파일 매핑  (🔗 매핑 / 실행)
----------------------------------------------------------------
"어느 Source 파일의 마크업을, 어느 Target 파일로 복사할지"를 정합니다.

방법 A) 직접 편집
  - [매핑 편집] 클릭 → 창에서 Source ↔ Target 짝을 확인/수정 → 확정.
  - Source 1개에 Target 여러 개를 연결할 수 있습니다(한 번에 여러 Train).

방법 B) Excel로 가져오기 (파일이 많을 때 편리)
  - [📂 Excel 매핑] 클릭 → 동봉된 '{TEMPLATE_XLSX}' 양식을 사용.
  - 양식 구조:
      · 1행 : 헤더(자유롭게)         · 2행 : 폴더 경로행
        A2 = Source 폴더 경로,  B2~ = 각 Train의 Target 폴더 경로
      · 3행~ : 파일명행
        A열 = Source 파일명,    B~열 = Train별 Target 파일명
  - 불러오면 폴더·매핑이 한 번에 채워집니다.

  ※ 매핑이 0쌍이면 실행되지 않습니다. 하단 "매핑: N 쌍" 표시를 확인하세요.
  ※ [🗑 초기화]로 매핑을 지울 수 있습니다.


----------------------------------------------------------------
3. 필터 사용법  (🎯 마크업 필터 — 선택)
----------------------------------------------------------------
"특정 마크업만" 골라서 복사하고 싶을 때 사용합니다(안 쓰면 전체 복사).

  - [✓ 필터 사용] 체크 후 조건 입력:
      · 기준 날짜 : YYYY-MM-DD (예: 2026-05-08)
                   → 그 날짜까지 작성된 마크업만 복사합니다.
      · 작성자(사번) : 마크업 작성자 사번과 정확히 일치하는 것만 복사.
  - 두 칸 중 비워둔 조건은 무시됩니다(둘 다 비우면 필터 효과 없음).


----------------------------------------------------------------
4. 정밀(위치) 보정  (📐 도면 위치 보정 — 선택)
----------------------------------------------------------------
Source와 Target의 도면 배치(CAD 레이아웃)가 서로 다를 때,
마크업 위치를 자동으로 맞춰 줍니다.

  - [✓ 위치 보정 사용] 체크.
  - 가장 정확한 방법(권장): 기준점을 직접 찍기
      1) Bluebeam에서 Source와 Target 도면의 "같은 지점"에
         마젠타색(RGB 255,0,255) 작은 마크업을 찍어 둡니다.
      2) Source와 Target에 같은 개수를, 같은 순서로 찍습니다.
      3) 정확히 맞춰야 하는 영역(예: 계장 Tag, 분기점) 근처에
         기준점을 더 촘촘히 찍을수록 그 주변이 정확해집니다.
  - 기준점을 안 찍으면, 배관선번호·계측기·심볼·도면 모서리를
    자동으로 찾아 기준점으로 사용합니다.
  - 추가로, 계장 Tag는 심볼 원 중심에, Reducer 등은 배관선 위에
    "가까울 때만" 자동으로 달라붙도록 보정합니다.

  ※ 이 모드는 Bluebeam 자동화 없이 PDF에 직접 적용되어 안전합니다.


----------------------------------------------------------------
5. 실행 / 결과 확인
----------------------------------------------------------------
  - [🚀 실행] 을 누르면 작업이 시작되고, 오른쪽 '작업 로그'에
    진행 상황이 표시됩니다([⏹ 중지]로 중단 가능).
  - 끝나면 Output 폴더가 자동으로 열립니다.
  - 결과가 이상하면, 오른쪽 '작업 로그' 전체를 복사해 배포자에게
    문의하세요(원인 파악에 로그가 가장 중요합니다).
"""


def main():
    if not os.path.isfile(EXE_PATH):
        print(f"❌ {EXE_PATH} 가 없습니다. 먼저 'python build.py' 로 EXE를 만드세요.")
        sys.exit(1)

    os.makedirs("dist", exist_ok=True)
    with zipfile.ZipFile(ZIP_PATH, "w", zipfile.ZIP_DEFLATED) as z:
        # 1) 실행 파일
        z.write(EXE_PATH, arcname=f"{APP_NAME}.exe")
        # 2) 사용법 안내
        z.writestr("사용법.txt", USAGE_TEXT)
        # 3) 매핑양식(있을 때만)
        if os.path.isfile(TEMPLATE_XLSX):
            z.write(TEMPLATE_XLSX, arcname=TEMPLATE_XLSX)
            print(f"   + 매핑양식 포함: {TEMPLATE_XLSX}")
        else:
            print(f"   (매핑양식 {TEMPLATE_XLSX} 없음 — 건너뜀)")

    size_mb = os.path.getsize(ZIP_PATH) / (1024 * 1024)
    print(f"\n✅ 배포 패키지 생성: {ZIP_PATH}  ({size_mb:.1f} MB)")
    print("   이 zip 파일 하나만 배포하면 됩니다.")


if __name__ == "__main__":
    main()
